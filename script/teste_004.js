let num1 = 42, num2 = 19, nome = "Geek";

console.log(num1, num2, nome);

//soma
let soma = num1 + num2;
console.log("O resultado da soma é: " + soma);
console.log(nome);

//subtração
sub = num1 - num2;
console.log("O resultado da subtração é: " + sub);

// multiplicação
mult = num1 * num2;
console.log("O resultado da multiplicação é: " + mult);

// divisão
mult = num1 / 2;
console.log("O resultado da divisão é: " + mult);

// modulo (resto divisão)
mult = num1 % 2;
console.log("O resto da divisão é: " + mult);

//incremento
//num1 = num1 + 8;
num1 += 8; //-=, *=, /=
console.log(num1)

console.log(num1++); // imprime a variável e depois incrementa 50, 51
console.log(num1--); // imprime a variável e depois decrementa 51, 50
console.log(++num1); // incrementa e depois imprime
console.log(--num1); // decrementa e depois imprime

